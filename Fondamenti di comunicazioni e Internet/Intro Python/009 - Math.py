import math

a = 8
b = 2

print(math.sqrt(a))         #Radice quadrata

print(math.cbrt(a))         #Radice cubica

print(math.pow(a, b))       #Elevamento a potenza

print(math.exp(a))          #Esponenziale e^x

c = 33.70

print(math.ceil(c))         #Arrotondamento per eccesso
print(math.floor(c))        #Arrotondamento per difetto

print(math.sin(1.5707963267948966))     #sin(x) input in radianti
print(math.cos(0))                      #cos(x) input in radianti
print(math.tan(0.7853981633974483))     #tan(x) input in radianti

print(math.degrees(1.0471975511965976))     #Radianti -> Gradi
print(math.radians(45))                     #Gradi -> Radianti